 * @property-read Collection<int, \App\Models\Booking> $mentorBookings
 * @property-read int|null $mentor_bookings_count
 * @mixin \Eloquent
 */
final class User extends Authenticatable
{
    use HasApiTokens;
    use HasFactory;
    use HasProfilePhoto;
    use Notifiable;
    use TwoFactorAuthenticatable;
    use SoftDeletes;

    public const MENTOR_USER_TYPE = 'mentor';
    public const MENTEE_USER_TYPE = 'mentee';

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'profile_photo_path',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
        'two_factor_recovery_codes',
        'two_factor_secret',
    ];

    /**
     * The accessors to append to the model's array form.
     *
     * @var array<int, string>
     */
    protected $appends = [
        'profile_photo_url',
    ];

    protected $casts = [
        'languages' => 'array',
    ];

    /**
     * Get the attributes that should be cast.
     *
     * @return array<string, string>
     */
    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password' => 'hashed',
        ];
    }

    public function mentorBookings(): HasManyThrough
    {
        return $this->hasManyThrough(
            Booking::class,
            Mentor::class,
            'user_id',
            'mentor_id',
            'id',
            'id'
        );
    }

    public function menteeBookings(): HasMany
    {
        return $this->hasMany(Booking::class, 'user_id');
    }

    public function mentor(): HasOne
    {
        return $this->hasOne(Mentor::class);
    }
}
